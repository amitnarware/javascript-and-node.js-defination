//Array Methods:

/*
1.  push() and pop(): Used to add and remove elements from the
 end of an array, respectively.

2..unshift() and shift(): Used to add and remove elements from
 the beginning of an array, respectively.

3..concat(): Combines two or more arrays to create a new array.


4..join(): Joins all elements of an array into a single string
 using a specified separator.

5.slice(): Creates a new array containing a copy of a portion 
of the original array.

6..splice(): Adds or removes elements from an array at a
 specified index.

7..forEach(): Iterates through each element of the array and 
applies a provided function.

8.map(): Creates a new array by applying a provided function
 to each element in the original array.

9.filter(): Creates a new array with elements that satisfy a
 specified condition.

10.reduce() and reduceRight(): Reduces an array to a single 
value by applying a provided function from left to right (or right to left).

11.find() and findIndex(): Find the first element or its index
 that matches a specified condition.

12.some() and every(): Check if at least one or all elements
 in an array meet a specified condition.

13.sort(): Sorts the elements of an array in place or creates
 a new sorted array.

14.reverse(): Reverses the order of elements in an array.

15.indexOf() and lastIndexOf(): Find the index of the first or
 last occurrence of a specified element.  */