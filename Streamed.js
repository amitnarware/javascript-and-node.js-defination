  // STREAMS IN NODE.JS


  /* Streams are one of the fundamental concepts of Node.js.
   Streams are a type of data-handling methods and are used to read
or write input into output sequentially. Streams are used to
handle reading/writing files or exchanging information in an 
efficient way.

The official Node.js documentation defines streams as “A stream is 
an abstract interface for working with streaming data in Node.js.” 
The stream module provides an API for implementing the stream

interface. Examples of the stream object in Node.js can be a 
request to an HTTP server and process.stdout are both stream
 instances. In short, Streams are objects in Node.js that lets the
  user read data from a source or write data to a destination in 
  a continuous manner. */