/*Primitive Data Types:
These are the most basic data types in JavaScript and are 
immutable (cannot be changed).

1..Number: Represents numeric values. It can be an integer or
 a floating-point number.
2..String: Represents a sequence of characters, such as text.
 Strings are enclosed in single or double quotes.
3..Boolean: Represents a true or false value.
4..Undefined: Represents a variable that has been declared but
 hasn't been assigned a value.
5..Null: Represents the intentional absence of any value or
 object.
6..Symbol (ES6): Represents a unique and immutable value,
 often used as object property keys.
7..BigInt (ES11): Represents large integers that cannot be
 represented by the Number data type.

 Reference Data Types:


These data types are more complex and can hold multiple values
 or other objects. They are mutable (can be changed).

1..Object: Represents a collection of key-value pairs, where
 keys are strings (or Symbols) and values can be of any data
  type, including other objects.
2..Array: A special type of object used to store lists of
 values, accessible by their numeric index.
3..Function: A callable object that can execute a block of
 code.
4..Date: Represents dates and times.
5..RegExp: Represents regular expressions for pattern matching within strings.
Map (ES6): A collection of key-value pairs with iteration order based on the order of insertion.
Set (ES6): A collection of unique values with no duplicates.
WeakMap (ES6): Similar to Map but with weaker object references, making it easier for objects to be garbage collected.
WeakSet (ES6): Similar to Set but with weaker object references.  */
