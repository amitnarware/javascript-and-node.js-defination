// WHAT IS CLUSTER

/*  In Node.js, the "cluster" module is a built-in module that allows you to create
 multiple child processes (workers) to take advantage of multi-core CPUs. It is
used to improve the performance and scalability of Node.js applications by 
distributing the workload across multiple processes. Each child process is a 
separate instance of the Node.js runtime, running concurrently and sharing the 
same server port, allowing you to handle more requests in parallel.

*/